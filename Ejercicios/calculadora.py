A = float(input("Ingresar valor del Alto del rectangulo: "))
H = float(input("Ingresar el valor del Hancho del rectangulo: "))

area_rectangulo= (A*H)
Perimetro_rectangulo=(2*A)+(2*H)
superficie_rectangulo=A*H

print(f"El area del rectangulo es: {area_rectangulo}")
print(f"El perimetro del rectangulo es: {Perimetro_rectangulo}")
print(f"La superfice del rectangulo es: {superficie_rectangulo}")